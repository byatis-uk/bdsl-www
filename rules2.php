 </tr>
 <tr><td class="normalcenter" colspan="2"><br>End of Season<br><br></td></tr>
 <tr>
  <td class="normal"><a name="12"></a><b>12</b></td>
  <td class="normal">Teams who are deemed to have caused fixtures to remain not played by the end of the season deadline (Rule 11) will for every not played fixture will have two points deducted from their end of season total, plus the same number of points deducted from their total at the start of the next season therefore starting with a negative points total.<br>This rule will <b>not</b> be applied to teams who have not fulfilled fixtures through no fault of their own.</td>
 </tr>
 <tr><td class="normalcenter" colspan="2"><br>Post-Match<br><br></td></tr>
 <tr>
