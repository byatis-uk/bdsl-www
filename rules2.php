 </tr>
 <tr><td class="normalcenter" colspan="2"><br>Pre-Match<br><br></td></tr>
 <tr>
  <td class="normal"><a name="14"></a><b>14</b></td>
  <td class="normal">The only playing balls and pins permitted are those used by the home team and shall be used by both teams. Front-pin Knockout and All-in Cup competitions - neutral alley, balls and pins to be used unless mutually agreed by the two opposing captains. </td>
 </tr>
 <tr><td class="normalcenter" colspan="2"><br>Playing Rules<br><br></td></tr>
 <tr>
  <td class="normal"><a name="15"></a><b>15</b></td>
  <td class="normal">All balls must strike the alley before a line drawn across the width of the alley. This line is called a "ball" line. In the event of more than one line being drawn across the alley, the line nearest to the pins is to be taken as the ball line. If the ball strikes the alley after the line it is a "foul" ball.  It is recommended that there shall be eight feet behind this line which the players can bowl from.</td>
 </tr>
 <tr>
  <td class="normal"><a name="16"></a><b>16</b></td>
  <td class="normal">Pins knocked down or clear of the diamond to count. Any pin that is struck and passes through the horizontal plane and stands up again will be counted as down. Cush or foul balls to count but pins must be stuck back up again.</td>
 </tr>
 <tr>
  <td class="normal"><a name="17"></a><b>17</b></td>
  <td class="normal">Any team a player short shall take the lowest score from each leg scored by either side as the missing players score; no team play with less than seven players.<br>Two legs only for late arrival of a player allowed; after the start of the third leg, the late arrival forfeits the legs already played with the score of zero, unless otherwise mutually agreed by the captains before the game commenced.</td>
 </tr>
 <tr><td class="normalcenter" colspan="2"><br>Post-Match<br><br></td></tr>
 <tr>
  <td class="normal"><a name="18"></a><b>18</b></td>
  <td class="normal">Any team playing an ineligible player loses points to the opposite side.</td>
 </tr>
 <tr>
  <td class="normal"><a name="19"></a><b>19</b></td>
  <td class="normal">Should any team fail to complete its fixtures by withdrawing from the league during the playing season, and before completion of the season, all games played by that team to the date of its withdrawal shall be deemed null and void and no points awarded.</td>
 </tr>
 <tr><td class="normalcenter" colspan="2"><br>Competitions<br><br></td></tr>
