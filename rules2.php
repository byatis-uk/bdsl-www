 <tr>
  <td class="normal"><a name="10"></a><b>10</b></td>
  <td class="normal">That 48 hours notice be given to cancel a match; less than 48 hours given will incur a loss of the two points and will be credited to the opposing side, unless this is due to extreme adverse weather conditions or other unforeseen circumstances as agreed with the results secretary. Cancelled games must be brought to the notice of the appropriate results secretary, opposing captain, alley concerned also the sticker.</td>
